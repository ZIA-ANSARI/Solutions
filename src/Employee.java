
public class Employee {
int name;
int city;
int phone;
public int getName() {
    return name;
}
public void setName(int name) {
    this.name = name;
}
public int getCity() {
    return city;
}
public void setCity(int city) {
    this.city = city;
}
public int getPhone() {
    return phone;
}
public void setPhone(int phone) {
    this.phone = phone;
}
}
